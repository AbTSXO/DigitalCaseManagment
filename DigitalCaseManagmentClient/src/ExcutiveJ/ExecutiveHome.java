    private void searchcase1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchcase1ActionPerformed
        String casenumber;
        ArrayList singlecaselist=new ArrayList();

        casenumber=viewSingleCaseNumber1.getText();
        if(casenumber.equals("")){JOptionPane.showMessageDialog(null,"fill the case number");}
        else{
            try {
                singlecaselist=(ArrayList)service.viewCase(casenumber);
                try {
                    service.logresgister(ipaddress,username, "check case to edit", casenumber);
                } catch (RemoteException ex) {
                    Logger.getLogger(ExecutiveHome.class.getName()).log(Level.SEVERE, null, ex);
                }
            } catch (RemoteException ex) {
                Logger.getLogger(ExecutiveHome.class.getName()).log(Level.SEVERE, null, ex);
            }
            if(singlecaselist.size()>1)
            {
                // JOptionPane.showMessageDialog(null,"case found");
                casetitleedit1.setText((String) singlecaselist.get(0));
                summarycaseedit1.setText((String) singlecaselist.get(1));
                judgecase1.setText((String) singlecaselist.get(2));
                updatecaseview.setVisible(true);

            }
            else
            {
                JOptionPane.showMessageDialog(null,"case not sfound");
            }
        }
    }//GEN-LAST:event_searchcase1ActionPerformed

    private void updatecaseviewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updatecaseviewActionPerformed
        String casenumber,ctitile,cdesc,cjudge;
        String result = null;
        casenumber=viewSingleCaseNumber1.getText();
        ctitile=casetitleedit1.getText();
        cdesc=summarycaseedit1.getText();
        cjudge=judgecase1.getText();

        if((ctitile.equals(""))||(cdesc.equals(""))|| cjudge.equals("")){JOptionPane.showMessageDialog(null,"fill out empty fields");}
        else{
            try {
                result=service.updatecase(casenumber,ctitile,cdesc,cjudge);
            } catch (RemoteException ex) {
                Logger.getLogger(ExecutiveHome.class.getName()).log(Level.SEVERE, null, ex);
            }
            if(result.equals("successful operation"))
            {
                try {
                    service.logresgister(ipaddress,username, "Update case information", casenumber);
                } catch (RemoteException ex) {
                    Logger.getLogger(ExecutiveHome.class.getName()).log(Level.SEVERE, null, ex);
                }
                JOptionPane.showMessageDialog(null,"successful operation");
                casetitleedit1.setText("");
                summarycaseedit1.setText("");
                judgecase1.setText("");
                updatecaseview.setVisible(false);
            }
            else{JOptionPane.showMessageDialog(null,"unsuccessful operation");}

        }
        // TODO add your handling code here:
    }//GEN-LAST:event_updatecaseviewActionPerformed

