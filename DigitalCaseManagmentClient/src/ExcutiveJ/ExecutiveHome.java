    private void searchcaseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchcaseActionPerformed
        String casenumber;
        ArrayList singlecaselist=new ArrayList();

        casenumber=viewSingleCaseNumber.getText();
        if(casenumber.equals("")){JOptionPane.showMessageDialog(null,"fill the case number");}
        else{
            try {
                singlecaselist=(ArrayList)service.viewCase(casenumber);
                try {
                    service.logresgister(ipaddress,username, "check case to drop", casenumber);
                } catch (RemoteException ex) {
                    Logger.getLogger(ExecutiveHome.class.getName()).log(Level.SEVERE, null, ex);
                }
            } catch (RemoteException ex) {
                Logger.getLogger(ExecutiveHome.class.getName()).log(Level.SEVERE, null, ex);
            }
            if(singlecaselist.size()>1)
            {
                casetitleedit.setText((String) singlecaselist.get(0));//set the case title input field by arraylsit first item
                summarycaseedit.setText((String) singlecaselist.get(1));//set the case description input field by arraylsit secomd item
                judgecase.setText((String) singlecaselist.get(2));//set the case Assigned judge input field by arraylsit third item
                drop.setVisible(true);//maker the drop case button visibles
                
                casetitleedit.setEditable(false);//make the title input field non editable
                summarycaseedit.setEditable(false);//make the summary case input field non editable
                judgecase.setEditable(false);//make the Assigned judge input field non editable
                viewSingleCaseNumber.setEditable(false);//make the cazse number input field non editable
                
            }
            else
            {
                JOptionPane.showMessageDialog(null,"case not found");
            }
        }
    }//GEN-LAST:event_searchcaseActionPerformed

