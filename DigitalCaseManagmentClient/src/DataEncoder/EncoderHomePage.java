    private void registerdefendantActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registerdefendantActionPerformed
        String fname,lname,address,phone,personid,casenumber,cata;
        String result = null;
        fname=dfname.getText();
        lname=dlname.getText();
        address= daddress.getText();
        phone=dphonenumber.getText();
        personid=dpersonid.getText();
        casenumber=dcasenum.getText();
        String logdata=casenumber+","+personid;
        cata="Defendant";
        if((fname.equals(""))|| (lname.equals(""))|| (address.equals(""))|| (phone.equals("")) || (personid.equals(""))|| (casenumber.equals("")))
        {

            JOptionPane.showMessageDialog(null,"please enter the empty field");

        }
        else{

            try {
                result=service.updatecasedetail(casenumber,cata,fname,lname,address,personid,phone);
            } catch (RemoteException ex) {
                Logger.getLogger(EncoderHomePage.class.getName()).log(Level.SEVERE, null, ex);
            }
            if(result.equals("successful operation"))
            {
                try {
                    service.logresgister(ipaddress,username, "Register defendant", logdata);
                } catch (RemoteException ex) {
                    Logger.getLogger(EncoderHomePage.class.getName()).log(Level.SEVERE, null, ex);
                }
                dfname.setText("");//clear first name field after successful operation
                dlname.setText("");//clear last name field after successful operation
                daddress.setText("");//clear Address field after successful operation
                dphonenumber.setText("");//clear phone field after successful operation
                dpersonid.setText("");//clear person id field after successful operation
                dcasenum.setText("");//clear case number field after successful operation
                registerdefendant.setVisible(false);
                plantiffinformation.setVisible(false);
                JOptionPane.showMessageDialog(null,"successful operation");
            }
            else{
                JOptionPane.showMessageDialog(null,"unsuccessful operation");
            }

        }
    }//GEN-LAST:event_registerdefendantActionPerformed

