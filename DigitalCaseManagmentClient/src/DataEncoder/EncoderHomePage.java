    private void searchcaseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchcaseActionPerformed
        String casenumber;
        ArrayList singlecaselist=new ArrayList();

        casenumber=viewSingleCaseNumber.getText();
        if(casenumber.equals("")){JOptionPane.showMessageDialog(null,"fill the case number");}
        else{
            try {
                singlecaselist=(ArrayList)service.viewCase(casenumber);
                try {
                    service.logresgister(ipaddress,username, "check case to edit", casenumber);
                } catch (RemoteException ex) {
                    Logger.getLogger(EncoderHomePage.class.getName()).log(Level.SEVERE, null, ex);
                }
            } catch (RemoteException ex) {
                Logger.getLogger(EncoderHomePage.class.getName()).log(Level.SEVERE, null, ex);
            }
            if(singlecaselist.size()>1)
            {
                // JOptionPane.showMessageDialog(null,"case found");
                casetitleedit.setText((String) singlecaselist.get(0));
                summarycaseedit.setText((String) singlecaselist.get(1));
                judgecase.setText((String) singlecaselist.get(2));
                updatecaseview.setVisible(true);

            }
            else
            {
                JOptionPane.showMessageDialog(null,"case not sfound");
            }
        }
    }//GEN-LAST:event_searchcaseActionPerformed

